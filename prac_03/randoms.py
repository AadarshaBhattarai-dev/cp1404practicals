import random

# Line 1
print(random.randint(5, 20))
# Smallest number: 5, largest number: 20

# Line 2
print(random.randrange(3, 10, 2))
# Could line 2 have produced a 4? No, because 4 is not in the step sequence.

# Line 3
print(random.uniform(2.5, 5.5))
# Smallest number: 2.5, largest number: 5.5 (any float in between)

# Produce a random number between 1 and 100 inclusive
print(random.randint(1, 100))
